def anya_say():
    import sys
    if len(sys.argv) < 2:
        print("No se ha proporcionado un mensaje, modo de uso: python anyaSay.py cualquier texto")
        return

    mensaje = sys.argv[1]
    caracteres = len(mensaje)
    longitud = caracteres // 52 + 1

    print(" " + "_" * 54)
    print("/" + " " * 54 + "\\")

    if caracteres <= 51:
        print("| " + mensaje + (" " * (53 - caracteres)) + "|")
    elif caracteres >= 52:
        for i in range(1, longitud):
            inicial = 1
            multiplicar = i - 1
            if i > 1:
                inicial = 0
            print("| " + mensaje[multiplicar * 52 + inicial: i * 52] + " |" + "\n" + "|" + " " * 54 + "|")
            if i + 1 == longitud:
                local = len(mensaje[i * 52:])
                print("| " + mensaje[i * 52:] + " " * (53 - local) + "|")

    print("\\ " + "_" * 53 + "/")
